#include "LED.h"

u16 LED_Tube[10] = {0xC000,0xF900,0xA400,0xB000,0x9900,0x9200,0x8200,0xF800,0x8000,0x9000};

void LED_Init(){
    //使能GPIOA的时钟
    GPIO_PortClock(GPIOA,true);
    
    //定义GPIOA1~4为输出模式
    GPIO_PinConfigure(GPIOA,1,GPIO_OUT_PUSH_PULL,GPIO_MODE_OUT50MHZ);
    GPIO_PinConfigure(GPIOA,2,GPIO_OUT_PUSH_PULL,GPIO_MODE_OUT50MHZ);
    GPIO_PinConfigure(GPIOA,3,GPIO_OUT_PUSH_PULL,GPIO_MODE_OUT50MHZ);
    GPIO_PinConfigure(GPIOA,4,GPIO_OUT_PUSH_PULL,GPIO_MODE_OUT50MHZ);
    GPIO_PinConfigure(GPIOC,0,GPIO_OUT_PUSH_PULL,GPIO_MODE_OUT50MHZ);

    
    //定义GPIOA1~4为高电平
    GPIO_PinWrite(GPIOA,1,1);
    GPIO_PinWrite(GPIOA,2,1);
    GPIO_PinWrite(GPIOA,3,1);
    GPIO_PinWrite(GPIOA,4,1);

}

